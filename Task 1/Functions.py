#Функція, яка видаляє слова від другого до передостаннього
def rm_md_words(s):

    #Розділяємо рядок на слова за комою й пробілом
    words = [w.strip() for w in s.split(",")]

    #Якщо менше 3 слів, повертаємо без змін
    if len(words) < 3:
        return s
    
    #Залишаємо тільки перше і останнє слово
    new_words = [words[0], words[-1]]

    #Об'єднуємо назад у рядок через ", "
    return ", ".join(new_words)